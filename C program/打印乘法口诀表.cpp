# include <stdio.h>

printf_table(int n)
{
	int i = 0;
	
	for (i=1; i<=n; ++i)                                   //行数
	{
		int j = 0;
		
		for (j=1; j<=i; j++)                               //行数等于列数
		{
			printf("%d*%d=%-2d ", i, j, i * j);            //%-2d ：将数字按宽度为2，采用左对齐方式输出，若数据位数不到2位，则右边补空格
		}
		printf("\n");
	}
}

int main(void)
{
	int n = 0;

	printf("请输入乘法表的类型：");
	scanf("%d", &n);
	
	printf_table(n);
	
	return 0;
}
