# include <stdio.h>

int main(void)
{
	int i;
	int j;

	for (i=0; i<3; i++)           
        printf("桀桀桀！\n");
	    for (j=2; j<5; j++)
			printf("哈哈哈！\n");
	printf("嘻嘻嘻!\n");

	return 0;

}
