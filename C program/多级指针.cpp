# include <stdio.h>

int main(void)
{
	int i = 8;

	int * p = &i;     //一级指针,  p --> i;  *p == i    

	int ** q = &p;    //二级指针，*q --> p;  **q == *p

	int *** r = &q;   //三级指针, **r --> *q;  ***r == **q  ,不确定

	printf("%d\n", ***r);

	return 0;
}
