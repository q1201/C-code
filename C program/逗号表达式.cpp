# include <stdio.h>

int main(void)
{
	int i;
	int j = 2;

	i = (j++, ++j, j+2, j-3);   //j++ = 2+1 = 3, ++j = 3+1 = 4, j+2 = 4+2 = 6, j-3 = 4-3 = 1  
	printf("%d\n", i);


	return 0;
}
/*
VC++6.0输出结果：
-----------------
       1
-----------------
结论：从左到右执行，最终表达式的值是最后一项的值
*/